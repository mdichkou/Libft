#include "libft.h"

int		ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned	char	*temp1;
	unsigned	char	*temp2;

	temp1 = (unsigned	char*)s1;
	temp2 = (unsigned	char*)s2;
	while (n > 0)
	{
		if (*temp1 != *temp2)
			return (*temp1 - *temp2);
		temp1++;
		temp2++;
		n--;
	}
	return (0);
}
